    return;
  }
  await showLoading.start("жӯЈеңЁж·»еҠ з”ЁжҲ·", "жӯЈеңЁе°қиҜ•еҲ·ж–°Cookie");
  const ck: TGApp.App.Account.Cookie = {
    account_id: ckRes.stuid,
    ltuid: ckRes.stuid,
    stuid: ckRes.stuid,
    mid: ckRes.mid,
    cookie_token: "",
    stoken: ckRes.stoken,
    ltoken: "",
